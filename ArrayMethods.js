// All the array Method which are essentials and used frequently;

array.length();
// this returns the length of an array;

array.pop();
// this remove an element of array from last index;

array.push();
// this adds an element to array from last index;

array.shift();
// this removes the element of array from first index:

array.unshift();
// this adds an element to an array from first index/ at the Beginning.

array.slice();
// this slice out a part/elements of an array into a new array;

array.splice();
// this will add a new items to an array;
// And also With clever parameter setting we can remove elements
// without leaving "holes" in the array.

array.concat();
// this will creates a new array by merging / concat existing array;

array.toString();
// this will convert an array to a comma separated string. 

array.at();
// this method will help you to get or target the element "at" that index position

array.join();
// this will help you to convert all the element in to string,
//and joins all the elements of array and takes a seperator argument

array.delete();
// this will help you to delete the element at speacified/targeted index this may leave empty places or holes in array.

array.isArray();
// this method is use to identify the value passed is an array or not.
